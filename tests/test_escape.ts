define([
        'intern',
        'intern!object',
        'intern/chai!assert',
        'dist/amd/es6/IonHash',
    ],
    function(intern, registerSuite, assert, ionhash) {
        var suite = {
            name: 'escape'
        };

        suite['null'] = function() {
            assert(ionhash.escape([]), []);
            assert(ionhash.escape([0x10, 0x11, 0x12, 0x13]), [0x10, 0x11, 0x12, 0x13]);
            assert(ionhash.escape([0x0b]), [0x0c, 0x0b]);
            assert(ionhash.escape([0x0e]), [0x0c, 0x0e]);
            assert(ionhash.escape([0x0c]), [0x0c, 0x0c]);
            assert(ionhash.escape([0x0b, 0x0e, 0x0c]), [0x0c, 0x0b, 0x0c, 0x0e, 0x0c, 0x0c]);
            assert(ionhash.escape([0x0c, 0x0c]), [0x0c, 0x0c, 0x0c, 0x0c]);
            assert(ionhash.escape([0x0c, 0x10, 0x0c, 0x11, 0x0c, 0x12, 0x0c]), [0x0c, 0x0c, 0x10, 0x0c, 0x0c, 0x11, 0x0c, 0x0c, 0x12, 0x0c, 0x0c]);
        };

        registerSuite(suite);
    }
);
